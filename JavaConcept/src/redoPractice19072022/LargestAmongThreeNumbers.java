public class LargestAmongThreeNumbers {

	public static void main(String[] args) {
		int a =611;
		int b=152;
		int c=1;
		
		if(a>b && a>c) {
			System.out.println(a +" :is largest");
		}
		if(b>a && b>c) {
			System.out.println(b +" :is largest");
		}
		if(c>a && c>b) {
			System.out.println(c +" :is largest");
		}

	}

}
